"""A module defining the Calculator class"""
class Calculator:
    """The calculator class"""
    def __init__(self):
        pass

    def Add(self, value1, value2):
        """A method to add the first passed in number to the second one"""
        return value1 + value2

    def Substract(self, value1, value2):
        """A method to substract the second passed in number from the first one"""
        return value1 - value2

    def Multiply(self, value1, value2):
        """A method to multiply the first passed in number by the second one"""
        return value1 * value2

    def Divide(self, value1, value2):
        """A method to divide the first passed in number by the second one"""
        return value1 / value2
